function c32083046.rellimit(e,c,tp,sumtp)
	return c==e:GetHandler()
end
function c32083046.flimit(e,c,sump,sumtype,sumpos,targetp,se)
	return c:IsType(TYPE_FUSION)
end
function c32083046.ffilter(c)
	return c:IsFaceup() and c:IsType(TYPE_FUSION) and c:IsSetCard(0x7D53)
end
function c32083046.fcondition(e)
	return Duel.IsExistingMatchingCard(c32083046.ffilter,e:GetHandlerPlayer(),LOCATION_MZONE,0,1,e:GetHandler())
end
